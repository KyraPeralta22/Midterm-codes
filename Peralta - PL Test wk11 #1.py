# Initialization of Sets
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

print("Sets:")
print("A = ", A)
print("B = ", B)

# Operators
print("\nSet Union:")
print(A | B, "\n")
print("Set Intersection:")
print(A & B, "\n")
print("Set Difference:")
print(A - B, "\n")
print("Set Symmetric Difference:")
print(A ^ B, "\n")


